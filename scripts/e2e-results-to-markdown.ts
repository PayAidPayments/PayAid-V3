/**
 * Reads e2e-results.json (Playwright JSON reporter output) and writes E2E_RESULTS.md
 * for sharing in chat or docs. Run after: PLAYWRIGHT_JSON_REPORT=1 npx playwright test --workers=1
 */
import * as fs from 'fs';
import * as path from 'path';

const resultsPath = path.resolve(process.cwd(), process.argv[2] || 'e2e-results.json');
const outPath = path.join(process.cwd(), 'E2E_RESULTS.md');

interface JSONReport {
  stats?: {
    startTime: string;
    duration: number;
    expected: number;
    unexpected: number;
    flaky: number;
    skipped: number;
  };
  suites?: Array<{
    title: string;
    file?: string;
    specs?: Array<{
      title: string;
      ok: boolean;
      tests?: Array<{
        title: string;
        status: string;
        results?: Array<{ error?: { message?: string } }>;
      }>;
    }>;
    suites?: JSONReport['suites'];
  }>;
}

function collectFailed(suites: JSONReport['suites']): Array<{ name: string; error?: string }> {
  const failed: Array<{ name: string; error?: string }> = [];
  if (!suites) return failed;
  for (const suite of suites) {
    if (suite.specs) {
      for (const spec of suite.specs) {
        for (const test of spec.tests || []) {
          if (test.status === 'unexpected') {
            const err = test.results?.[0]?.error?.message;
            failed.push({ name: test.title || spec.title, error: err });
          }
        }
      }
    }
    if (suite.suites?.length) {
      failed.push(...collectFailed(suite.suites));
    }
  }
  return failed;
}

if (!fs.existsSync(resultsPath)) {
  console.error('Results file not found:', resultsPath);
  console.error('Run: npm run test:e2e:share  (or pass a path, e.g. e2e-results-backups/e2e-results-2026-03-05T10-58-41.json)');
  process.exit(1);
}

const raw = fs.readFileSync(resultsPath, 'utf-8');
let report: JSONReport;
try {
  report = JSON.parse(raw);
} catch {
  console.error('Failed to parse e2e-results.json');
  process.exit(1);
}

const stats = report.stats || { expected: 0, unexpected: 0, flaky: 0, skipped: 0, duration: 0, startTime: '' };
const failed = collectFailed(report.suites);
const total = stats.expected + stats.unexpected + stats.flaky + stats.skipped;
const passed = stats.expected;
const durationSec = Math.round(stats.duration / 1000);

const runLabel = stats.startTime ? new Date(stats.startTime).toLocaleString() : 'N/A';
const lines: string[] = [
  '# E2E test results (localhost:3000)',
  '',
  `**Run:** ${runLabel} · **Duration:** ${durationSec}s`,
  '',
  '> This file is generated from `e2e-results.json`. That file is updated on every Playwright run, so re-run `npx tsx scripts/e2e-results-to-markdown.ts` after a test run to match the HTML report.',
  '',
  '## Summary',
  '',
  '| Status | Count |',
  '|--------|-------|',
  `| Passed | ${passed} |`,
  `| Failed | ${stats.unexpected} |`,
  `| Flaky | ${stats.flaky} |`,
  `| Skipped | ${stats.skipped} |`,
  `| **Total** | **${total}** |`,
  '',
];

if (failed.length > 0) {
  lines.push('## Failed tests', '');
  for (const { name, error } of failed) {
    lines.push(`- **${name}**`);
    if (error) lines.push(`  - \`${error.replace(/\n/g, ' ').slice(0, 200)}${error.length > 200 ? '…' : ''}\``);
  }
  lines.push('');

  lines.push(
    '---',
    '',
    '## Fix these (paste the block below into Cursor)',
    '',
    'Copy the following section and paste it into Cursor with the prompt: **"Fix all of these E2E failures in the app."**',
    '',
    '```',
    'E2E failures to fix:',
    ''
  );
  for (const { name, error } of failed) {
    const type = name.startsWith('[ROUTE]') ? 'ROUTE' : name.startsWith('[LINKS]') ? 'LINKS' : 'BUTTONS';
    const page = name.replace(/^\[ROUTE\]\s*/, '').replace(/^\[LINKS\]\s*/, '').replace(/^\[BUTTONS\]\s*/, '');
    lines.push(`- ${type} | Page: ${page}`);
    if (error) lines.push(`  ${error.replace(/\n/g, ' ').trim().slice(0, 300)}`);
  }
  lines.push('```', '');
}

lines.push('---', '', '*Generated by `scripts/e2e-results-to-markdown.ts` after `npm run test:e2e:share`*');

fs.writeFileSync(outPath, lines.join('\n'), 'utf-8');
console.log(`Wrote ${outPath} (run: ${runLabel}, ${passed} passed, ${stats.unexpected} failed, ${stats.flaky} flaky)`);
