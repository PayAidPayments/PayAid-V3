export function getCategoryLabel(cat: TemplateCategory): string {
  return CATEGORY_LABELS[cat] ?? cat
}

export function getTemplateById(id: string): SheetTemplate | undefined {
  return SHEET_TEMPLATES.find((t) => t.id === id)
}

export function getTemplatesByCategory(category: TemplateCategory): SheetTemplate[] {
  return SHEET_TEMPLATES.filter((t) => t.category === category)
}

/** Default name when using template; optionally includes current month/year */
export function getDefaultNameForTemplate(template: SheetTemplate): string {
  const base = template.name.replace(/\s*[–-]\s*India.*$/i, '').trim()
  if (template.nameIncludesDate) {
    const d = new Date()
    const month = d.toLocaleString('en-IN', { month: 'short' })
    const year = d.getFullYear()
    return `${base} – ${month} ${year}`
  }
  return base
}

export function filterTemplates(query: string): SheetTemplate[] {
  if (!query.trim()) return SHEET_TEMPLATES
  const q = query.toLowerCase().trim()
  return SHEET_TEMPLATES.filter(
    (t) =>
      t.name.toLowerCase().includes(q) ||
      t.description.toLowerCase().includes(q) ||
      t.category.toLowerCase().includes(q) ||
      t.id.toLowerCase().includes(q)
  )
}
